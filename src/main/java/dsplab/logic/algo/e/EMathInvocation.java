package dsplab.logic.algo.e;

public class EMathInvocation extends Exception
{
    public EMathInvocation()
    {
    }

    public EMathInvocation(String message)
    {
        super(message);
    }

    public EMathInvocation(String message, Throwable cause)
    {
        super(message, cause);
    }

    public EMathInvocation(Throwable cause)
    {
        super(cause);
    }
}
